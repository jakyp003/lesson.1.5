package com.company;

public class Boss {
    private int hp;
    private int dm;
    private String defent;

    public int getHp() {
        return hp;
    }

    public void setHp(int hp) {
        this.hp = hp;
    }

    public int getDm() {
        return dm;
    }

    public void setDm(int dm) {
        this.dm = dm;
    }

    public String getDefent() {
        return defent;
    }

    public void setDefent(String defent) {
        this.defent = defent;
    }
}
